# 2. 공백을 포함할 수 없다.
# 3. 공백 대신 언더바, 언더스코어_를 대신 사용한다.
# 4. 가능한 한 의미 있는 단어로 만들 것
# 5. 대소문자를 구분한다.
  # (myVar와 MyVar는 서로 다른 변수다.)

# 자료형
# 데이터를 다루기 위해 그 종류를 구분해 놓은 것.
# 숫자 데이터 -> 연산o
# 문자 데이터 -> 연산x
# 논리

# 자료형의 종류
# 1. 숫자형(Numeric)
# - 정수, 실수를 포함해 숫자와 마침표만으로 이루어진
# 숫자값을 의미한다.
# 예) 1, 3.5, 0.00001, 3951
# numeric = 정수와 실수를 통틀어 이르는 말
# 정수: integer
# 실수: float
3.5e+10
class(3.5)


# 2. 문자형(Character)
# 작은따옴표'' 혹은 큰따옴표""로 감싼 데이터를 의미한다.
# 예) "a", '', "hello world", '12321'
'abc'
"hello world"
"123213211"

# 3. 논리형(Logical)
# 참과 거짓 두 가지 값만 존재한다.
# T와 F로 축약하여 사용할 수 있다.
# TRUE는 1, FALSE는 0으로도 표현된다.
# TRUE, FALSE, T, F
T + T + T
F * T

# 자료형을 확인하는 기능(함수)
# class(대상값 혹은 변수)
class("asd")
class(T + T)