# 변수란
# 값(데이터)을 저장하기 위한 저장공간을 의미한다.
# 1. 변수는 변수명과 변수 값으로 이루어져 있는데,
# 변수명을 정하는 이유는, 변화무쌍한 변수의 값에
# 항상 일정한 이름으로 쉽고 편하게 접근하기 위해서
# 이름을 부여하는 것이다.
# 2. 의미가 없는 값에 이름을 부여함으로써
# 하나의 정보로 만들기 위해서 변수명을 붙인다.

# 변수의 특징
# 하나의 변수에는 하나의 값만 들어간다.
# 이미 값이 있던 변수에 다른 값을 넣으면(대입)
# 이전에 있던 값을 잊고
# 새로운 값을 기억한다.(덮어쓰기)

# 변수의 선언 및 대입
# 변수명 <- 값
age <- 31 # age라는 변수(저장공간)에 31이라는 값을 저장한다.
age # age에 담긴 값 보여줘
print(age)

# 변수명의 명명규칙
