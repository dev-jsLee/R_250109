if(score >= 90){
  print("A입니다.")
} else if(score >= 80){
  # 80점이상~90미만 -> B
  print("B입니다.")
} else if(score >= 70){
  # 70~80미만 -> C
  print("C입니다.")
} else {
  # 70점 미만 -> F
  print("F입니다.")
}

# 의도3
# 출력하려는 값을 변수에 담는 동작만
# if문 안에서 하는 것
# 출력을 모든 if문을 빠져나간 뒤 한다.(일괄처리)
score_char <- "F"
if(score >= 90){
  score_char <- "A"
} else if(score >= 80){
  # 80점이상~90미만 -> B
  score_char <- "B"
} else if(score >= 70){
  # 70~80미만 -> C
  score_char <- "C"
}
# 값을 연결하여 출력하는, 여러 값을 전달할 수 있는
# 출력문
# cat(값1, 값2,...)
# 값1 값2 ...
