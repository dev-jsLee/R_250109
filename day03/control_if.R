# 제어문
# 제어문이란 조건에 따라서 일정 코드 블록을 실행하거나
# 건너뛰거나 혹은 특정 코드 블록을 반복하게끔
# 코드의 흐름을 제어하는 문법을 의미한다.
# 1. 조건문
# if(조건식){조건식이 참일 때 실행할 코드}
# 조건식: 연산의 최종 결과값이 T/F로 도출되는 계산식을 의미한다.
# 조건문은 조건식의 결과에 따라
# T: 해당 코드 블록을 실행하거나
# F: 해당 코드 블록을 건너뛴다.

num <- -10

# 조건식이 참이면 해당 코드 블록에 진입하여
# 코드를 실행한다.
# 조건식이 거짓이면 진입을 하지 못하고
# 해당 코드 블록을 무시하고 건너뛴다.

if(num>0){
  print("num이 양수입니다.")
  print("안녕히 가십시오")
}

# 2. if-else문
# if문의 조건식이 거짓인 경우 실행할 코드를
# else문의 코드 블록에 적을 수 있다.
# if문 혹은 else문의 코드 블록 중 반드시 하나는 실행하게 된다.
# else는 집합에서 여집합의 개념과 같다.
# ※ else문은 단독으로 사용할 수 없다.
num1 <- 11

if(num1 > 10){
  print("10보다 큽니다.")
} else { 
  print("10보다 작거나 같습니다.")
}

# 3. if-else-if문
# 다중 조건문
# 앞선 조건식이 거짓인 경우, 다음 조건식을 검사하는 방식으로
# 실행되는 조건문이다.
# 가장 먼저 참이 되는 조건식의 코드 블록을 실행한다.

num2 <- 30

if(num2 >= 100){
  print("우와 100이상")
}else if(num2 >= 50){
  print("50이상이다")
}else if(num2 >= 30) {
  print("30이상이구나")
}else {
  print("나머지 경우")
}

